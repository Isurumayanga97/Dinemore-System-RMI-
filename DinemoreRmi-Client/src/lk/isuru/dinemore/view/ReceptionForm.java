    private void btnplace_orderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnplace_orderActionPerformed
       try {
            String ststus = "Send To Kichen";

            int qty = Integer.parseInt(jsqty.getValue() + "");

            RecepDTO orderDTO = new RecepDTO(
                    txtoid.getText(),
                    txtrid.getText(),
                    lbldate.getText(),
                    lbltime.getText(),
                    txtcustname.getText(),
                    txttp.getText(),
                    qty,
                    ststus
            );
            boolean addorder = ReceptionController.placeOrder(orderDTO);
            if (addorder) {
                JOptionPane.showMessageDialog(this, "Order Added");
                loadAllCustomers();
            } else {
                JOptionPane.showMessageDialog(this, "Failed");
            }
        } catch (Exception ex) {
            Logger.getLogger(ReceptionForm.class.getName()).log(Level.SEVERE, null, ex);
        }     
    }//GEN-LAST:event_btnplace_orderActionPerformed

