    private void loadAllCustomers() {
        dtm = (DefaultTableModel) tblorder.getModel();
        dtm.setRowCount(0);
        try {
            List<RecepDTO> orders = ReceptionController.getAllCustomers();
            if (orders == null) {
                return;
            }

            for (RecepDTO recepDTO : orders) {
                Object[] rowData = {
                    recepDTO.getOid(),
                    recepDTO.getCid(),
                    recepDTO.getTpNo(),
                    recepDTO.getTime(),
                    recepDTO.getQty(),
                    recepDTO.getStatus()
                };
                dtm.addRow(rowData);
            }

        } catch (Exception ex) {
            Logger.getLogger(ReceptionForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
    private void getClock() {
        new Thread(new Runnable() {
            @Override
            public void run() {
                while (true) {
                    Date curDate = new Date();
                    SimpleDateFormat dateFormat = new SimpleDateFormat("HH:mm:ss aa");
                    String currentTime = dateFormat.format(curDate);

                    lbltime.setText(currentTime);
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        JOptionPane.showMessageDialog(ReceptionForm.this, "Database Error...");
                    }
                }
            }
        }).start();
    }
    
    
